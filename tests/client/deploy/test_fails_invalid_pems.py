def test_fails_invalid_fullchain(tmp_path: pathlib.Path):
    pem = tmp_path.joinpath('fullchain.pem')
    pem.write_bytes(MOCK_FULLCHAIN)
    with pytest.raises(InvalidKey):
        validate_keys(tmp_path)


